var symbolBox = document.querySelector("#symbolBox");
var symbolBoxCopy = document.querySelector("#symbolBoxCopy");
document.body.onclick = function () {
    var x = window.scrollX;
    var y = window.scrollY;
    symbolBox.focus();
    window.scrollTo(x, y);
}
symbolBoxCopy.onclick = function () {
    symbolBox.select();
    document.execCommand("copy");
    this.innerHTML = "Copied!";
    this.onmouseout = function () {
        this.innerHTML = "Copy";
    };
    symbolBox.value = "";
}
Object.keys(font).forEach(function (key) {
    var header = document.createElement("h1");
    header.innerHTML = font[key].name;
    document.body.appendChild(header);
    var container = document.createElement("div");
    container.className = "flexbox row wrap top center";
    document.body.appendChild(container);
    font[key].symbols.forEach(function (symbol) {
        var element = document.createElement("entity");
        var character = String.fromCharCode(parseInt(symbol.replace("&#x", ""), 16));
        element.innerHTML = character;
        element.title = symbol + "\n" + "Click to Copy";
        if (font[key].images && font[key].images[symbol]) {
            element.style.backgroundImage = "url(" + font[key].images[symbol] + ")"
            element.style.backgroundPosition = "center"
            element.style.backgroundSize = "contain"
            element.style.backgroundRepeat = "no-repeat"
            element.style.color = "transparent"
            element.style.filter = "opacity(0.5) brightness(96)"
        } else if (font[key].nicknames) {
            element.setAttribute("nickname", font[key].nicknames[symbol]);
        }
        element.setAttribute("symbol", character);
        element.onclick = function () {
            const selection = window.getSelection();
            const range = document.createRange();
            const title = element.title;
            range.selectNodeContents(element);
            selection.removeAllRanges();
            selection.addRange(range);
            document.execCommand('copy');
            selection.removeAllRanges();
            element.title = "Copied!";
            element.onmouseout = function () {
                element.title = title;
            };
            symbolBox.value += character;
        }
        container.appendChild(element);
    })
});
